from tkinter import *
root=Tk()
root.geometry('450x320')
lb=Label(root)
lb.pack()
e1=Entry(root,width=10)
e1.place(x=50,y=20)
e2=Entry(root,width=10)
e2.place(x=250,y=20)
def plus():
    a=float(e1.get())
    b=float(e2.get())
    s='%f加%f等于%f'%(a,b,a+b)
    lb.config(text=s)
    lb.place(x=100,y=280)
def minus():
    a1=float(e1.get())
    b1=float(e2.get())
    s1='%f减%f等于%f'%(a1,b1,a1-b1)
    lb.config(text=s1)
    lb.place(x=100,y=280)
def multiply():
    a2=float(e1.get())
    b2=float(e2.get())
    s2='%f乘%f等于%f'%(a2,b2,a2*b2)
    lb.config(text=s2)
    lb.place(x=100,y=280)
def division():
    a3=float(e1.get())
    b3=float(e2.get())
    s3='%f除%f等于%f'%(a3,b3,a3/b3)
    lb.config(text=s3)
    lb.place(x=100,y=280)
var=IntVar()
c1=Radiobutton(root,text='加',variable=var,value=0,command=plus)
c1.pack()
c1.place(x=180,y=80)
c2=Radiobutton(root,text='减',variable=var,value=1,command=minus)
c2.pack()
c2.place(x=180,y=120)
c3=Radiobutton(root,text='乘',variable=var,value=2,command=multiply)
c3.pack()
c3.place(x=180,y=160)
c4=Radiobutton(root,text='除',variable=var,value=3,command=division)
c4.pack()
c4.place(x=180,y=200)
